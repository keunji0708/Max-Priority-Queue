import java.io.*;
import java.util.*;

class Node {
	private int key;
	private String value;

	public Node() {
	}

	public Node(int k, String v) {
		key = k;
		value = v;
	}

	public int getkey() {
		return key;
	}

	public String getvalue() {
		return value;
	}

	public void setkey(int k) {
		key = k;
	}

	public void setvalue(String v) {
		value = v;
	}
}

